// Primitive Data Types
public class DataTypesExample {
    public static void main(String[] args) {
        byte smallNumber = 10; // 1 byte
        int largeNumber = 10000; // 4 bytes
        char letter = 'A'; // 2 bytes
        boolean isJavaFun = true; // 1 bit
        float decimalNumber = 3.14f; // 4 bytes
        double largeDecimal = 19.99; // 8 bytes

        // Display the value
        System.out.println("Byte value: " + smallNumber);
        System.out.println("Integer value: " + largeNumber);
        System.out.println("Character value: " + letter);
        System.out.println("Boolean value: " + isJavaFun);
        System.out.println("Decimal value: " + decimalNumber);
        System.out.println("Double value: " + largeDecimal);
    }
}
